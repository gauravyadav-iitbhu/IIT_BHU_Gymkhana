                    <p class="text-center">“The only phenomenon in this world, immune to change is change.”</p>
                    Even a cursory observation of Indian socio-political life is enough to drive home the fact that we are emerging as a more progressive society, albeit a bit slowly. Evolution is the primary characteristic of any societal unit and nowhere is it more evident than in the youth of today. Armed with technology that aims to surpass all human capabilities, our youth today is surging forward towards a better and more efficient future.
                    </br>At one of the premier engineering institutions of the nation, a new chapter in inclusive growth of society has been successfully portrayed by the acceptance of a revamped constitution and the establishment of a student parliament to go with it. This is of even more significance in a city like Varanasi, which is a cultural mix of people from extremely diverse backgrounds. Ever since the faculty of engineering, at BHU was given the status of IIT in 2012, there had been no student body and with good reason. Large scale violence and politicization in the academic campus had led to dissolution of the student unions in 1997, and their replacement ten years later by Student Councils in 2007. A separate body for the newly formed IIT (BHU), Varanasi was bound to get formed sooner rather than later.
                    </br>The need for such a body was felt harder when there was some agitation among the students regarding certain academic norms which had led to wide spread academic disruption in 2014. The process for drafting the constitution of the newly upgraded institute was expedited and by the start of 2015, the constitution was ready.
                    </br>As directed by the constitution, elections were conducted for the selection of representatives of the students. The elegance with which the entire process was completed is commendable. Students from undergraduate years to postgraduates and doctorate scholars were provided proportional representation and the unexpected peace and calm with which the elections were completed left even the organisers stunned. The entire academic campus had been utilised very efficiently to form numerous polling booths and hardly was there any report of disorderliness. The fear that holding elections and forming a parliament might lead to derogatory politics disappeared in the almost exciting atmosphere on the day of the elections. The scale on which the postgraduate students participated was deeply encouraging.
                    </br>The first session of the parliament, in all its grandeur, was conducted centring around the ceremonial oath-taking formalities. Sixty three formally dressed students, all set to take over the mantle of developing a better, efficient, secure and transparent atmosphere inside the college campus; realising the massive responsibility that they have all agreed to carry; and willing to put in the hard work  required to do so; was a sight worth being seen.
                    </br>The seriousness, with which this entire endeavour was being taken, was evident in the second session itself, where the students sat in for a straight stretch of more than five hours deciding upon the thirteen standing committees which had to be formed inside the parliament. The dedication and vigour shown by the elected members left the supervising professors stunned (and happy). It must be pointed out here that even the constitution had been drafted by a student committee. The amount of effort and research that had been done into its making was visible in the way every little thing had been thought off, and committees had been formed to address every type of issue that the students might face. Starting from finance and festivals committee to hostels and general welfare committee, from academic affairs committees to training and placement committees had been formed. A separate committee has been formed to handle alumni affairs as well! All proceedings were meticulously documented and made available to the student populous in general, thus ensuring complete transparency.
                    </br>What the student body had been sceptical about, in the beginning of this process was how seriously this student body would be taken. All doubts were removed when the third session itself was attended in official capacity by the director, registrar, deans, training and placement in-charge, and representatives from the council of wardens. Such a huge response from officials in the administration was not just a show of good faith but also showed their acceptance and appreciation of the entire mechanism. This readiness of the administration to work with the students was recognised and welcomed by the students.
                    </br>The discussion gave a clear signal that the people in-charge would now have to pull up their socks. One of our alumni, Dr. Animesh Pathak, was present unofficially and was deeply impressed by the way the students interacted with the faculty members and the level of openness in the discussions. The inclusion of a student body into an institution’s decision making process; in spite of the parliament being a deliberative body only; on such a large scale was heartening. The discussion ranged from minute topics like hygiene and cleanliness of hostels, to dustbins on roadsides, to fairly complex issues like revision of academic curriculum. The intent to include students’ opinions in all such issues is gratifying and hopefully will be fruitful in bringing out some concrete changes that shape the future of this institute. Contrary to what was expected by the general student’s body, their suggestions were welcomed and debated upon openly, with a very positive outlook. What was particularly impressive was that deadlines were pulled up on all decisions and changes being accepted, and the administration was readily willing to adhere to them. 
                    </br>Also, the fact that change is a two way process was stressed upon and the administration also requested the members to discuss certain issues like control on alcoholism inside the campus, and registration of motor vehicles, two issues which had been plaguing the institute for a very long time.
                    </br>More than anything, the atmosphere of geniality, mutual confidence and acceptance was inspiring. Periodical amendments into the constitution displayed the capacity of this endeavour to evolve and develop into something profoundly more impactful than what it is of now.
                    </br>The parliament is bound to be a trendsetter, and is going to play a crucial role in deciding the fate of student-administration relationship for a long time to come. We realise that the parliament members have a huge responsibility thrust upon their shoulders. A lot of expectations have been drawn up from this experiment of sorts. We sincerely hope that its lives up to them.
